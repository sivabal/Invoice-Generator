package com.bill.beans;


public class SalesMade {
	
	private String itemName;
	private String date;
	private String lotNo;
	private String salesmanName;
	private String goodsTaken;
	private String goodsReturned;
	private String goodsSold;
	
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getLotNo() {
		return lotNo;
	}
	public void setLotNo(String lotNo) {
		this.lotNo = lotNo;
	}
	public String getSalesmanName() {
		return salesmanName;
	}
	public void setSalesmanName(String salesmanName) {
		this.salesmanName = salesmanName;
	}
	public String getGoodsTaken() {
		return goodsTaken;
	}
	public void setGoodsTaken(String goodsTaken) {
		this.goodsTaken = goodsTaken;
	}
	public String getGoodsReturned() {
		return goodsReturned;
	}
	public void setGoodsReturned(String goodsReturned) {
		this.goodsReturned = goodsReturned;
	}
	public String getGoodsSold() {
		return goodsSold;
	}
	public void setGoodsSold(String goodsSold) {
		this.goodsSold = goodsSold;
	}
	
	
}
